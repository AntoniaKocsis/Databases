CREATE TABLE ExaminationRooms
(roomID int NOT NULL,
roomNumber int NOT NULL,
hospitalID int NOT NULL
PRIMARY KEY (roomID)
FOREIGN KEY (hospitalID) REFERENCES Hospitals(hospitalID)
);
CREATE TABLE MedicalDevices
(medicalDevicesID int NOT NULL,
name varchar(50) NOT NULL,
PRIMARY KEY (medicalDevicesID)
);
CREATE TABLE AvailableMedicalDevices
(hospitalID int NOT NULL,
medicalDevicesID int NOT NULL,
PRIMARY KEY (hospitalID,medicalDevicesID),
FOREIGN KEY (hospitalID) REFERENCES Hospitals,
FOREIGN KEY (medicalDevicesID) REFERENCES MedicalDevices
);
